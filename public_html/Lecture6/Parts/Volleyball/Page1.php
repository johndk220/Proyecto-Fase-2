&nbsp <a><li> Cuando el balón se encuentra fuera de la cancha, el juego se detiene hasta que el árbitro lo diga.</a></li>

	&nbsp <a><li>Para realizar el saque, el jugador debe esperar que él árbitro suene el pito. </a></li>

	&nbsp <a><li>La puntuación se obtiene cuando el balón se encuentra en juego y cae en la cancha contraria.</a></li>

	&nbsp <a><li>El árbitro se encuentra a un lado de la malla, fuera de la cancha de juego. </a></li>

	&nbsp <a><li>En la cancha se deben encontrar 12 jugadores 6 por cada equipo.</a></li>

	&nbsp <a><li> Equipo que pierde una jugada, el otro equipo realiza el próximo saque. </a></li>

	&nbsp <a><li>Se cambia de jugador cuando el entrenador lo considere necesario.</a></li>

	&nbsp <a><li> Solo se permiten 7 cambios por cada set de 25. </a></li>

	&nbsp <a><li> Equipo que logre llegar primero a los 25 puntos, gana el set. De haber un empate a 24, equipo que logre irse alante por 2 puntos gana el set. Si ambos equipos estan empatados a 2 sets, se realiza un quinto set a 15 puntos. El juego se acaba cuando un equipo gana 3 sets.</a></li>

	&nbsp <a><li>Si el balón toca el suelo dentro del propio campo, tanto si el último toque corresponde a un jugador propio como a uno del equipo contrario, se consigue el punto. </a></li>
	
	&nbsp <a><li>Si el toque del balón es incorrecto y hay retención o doble toque se le otorga el punto al equipo contrario. </a></li>
  
