<h2> Saque </h2>
<img src="http://4.bp.blogspot.com/-Ye7ZNyY_yJA/UYGSkYyHXHI/AAAAAAAAACI/PN8xXu1rlUM/s1600/saque.jpg" alt="Saque" width="288" height="175">
<blockquote>El saque es el golpe más importante del tenis, ya que este da comienzo al punto, y su correcta aplicación puede permitir a la persona que saca quedar en una posición de ventaja tras la devolución o bien lograr un saque ganador o ace (punto ganado sin que el rival impacte la pelota), o que tras el impacto del adversario la pelota no llegue a pasar la red o ésta se vaya fuera de los límites de los ejes (en cuyo caso no se denomina ace, sino punto de saque). Al tener buen saque, el tenista aprende a acabar mejor los golpes efectuados sin que la bola toque suelo y pudiendo dificultarle al contrincante marcarle un punto después de que le hagan una cortada.</blockquote>
