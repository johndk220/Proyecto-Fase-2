<blockquote>La dejada o drop shot es un golpe en el que se le resta potencia a la pelota con la intención de que caiga lo más cerca posible de la red, del lado contrario. Se realiza habitualmente de drive, aunque es posible hacerlo también de revés. La preparación del golpe es similar a la preparación del drive (o revés), debiendo realizarse en el último momento, para sorprender al contrincante, que espera un tiro al fondo. Al momento del impacto, en lugar de realizarse el swing amplio, la raqueta debe caer de manera perpendicular a la pelota, con un giro de muñeca, para producir el efecto de goteo que hará a la pelota caer y pasar bien la red.</blockquote>
