	&nbsp <a><li> Un partido de tenis comienza con el saque de uno de los jugadores que debe golpear la bola de tal forma que bote dentro del cuadro diagonal respecto del lado del que saca; es decir, el jugador siempre debe servir (sacar) de forma cruzada.</a></li>
	
	&nbsp <a><li> Para cada saque el jugador que saca tiene dos oportunidades; si falla la primera, tiene otra más, a menos que al primer intento la pelota toque la malla y luego caiga en el cuadrado que le toca, lo que se conoce generalmente como "red", en este caso el jugador repite ese saque.</a></li>
	
	&nbsp <a><li> Después del saque ambos jugadores deberán pasar alternativamente la bola de un campo al otro en busca de que su rival no pueda devolverla antes de que rebote dos veces; si sucede esto se conseguirá un punto. </a></li>
	
	&nbsp <a><li> Solo le puedes dar un golpe a la pelota</a></li>

	&nbsp <a><li> Ganando 4 puntos se consigue un juego, y ganando 6 juegos se consigue un set. </a></li>

	&nbsp <a><li> El partido se gana con 2 sets (mejor de 3) en los torneos del ATP World Tour, y con 3, mejor de 5 en los torneos de Grand Slam.</a></li>
	
	&nbsp <a><li> Se juega 1vs1 o 2vs2 y 2vs2 mixtos.</a></li>
