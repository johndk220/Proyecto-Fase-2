&nbsp <a><li> El fin del juego es lograr más carreras que el rival para vencerlo. </a></li>

	&nbsp <a><li>Una carrera se obtiene cuando el corredor logra pisar las 1ª, 2ª y 3ª base llegando por último al ‘home’ (que sería algo así como la cuarta base, pero es el lugar de partida y de llegada), en orden y sin ser eliminado. </a></li>

	&nbsp <a><li>Un partido se juega a 9 entradas. Las entradas consisten de un turno de bateo y un turno de defensa por equipo. El equipo locatario siempre comienza defendiendo. </a></li>

	&nbsp <a><li>Durante una entrada el equipo defensivo dispone en el campo sus 9 jugadores. Un pitcher, un catcher, un defensa entre la 1ª y 2ª base (el 2ª base), otros entre la 2ª y 3ª (el shortstop), otro en la 3ª base, y luego 3 jugadores en el exterior. </a></li>

	&nbsp <a><li> Durante una entrada el equipo atacante irá pasando por el cajón de bateo de acuerdo al orden estipulado, intentando batear el lanzamiento del pitcher y llegar a la base sin ser eliminado. </a></li>

	&nbsp <a><li> La partida jamás podrá darse por finalizada en empate, si luego de las nueve entradas así quedase el tanteador, pues se deberá seguir jugando hasta que uno de los dos obtenga la victoria.  </a></li>

	&nbsp <a><li>Si el equipo defensor logra eliminar a tres atacantes obtiene el derecho de ser atacante y viceversa. Cuando ello sucede se acaba ½ entrada. Cuando los dos equipos atacaron y defendieron finaliza la entrada completa. </a></li>

	&nbsp <a><li>  El bateador es eliminado : por un Strike-out (3 strike), Cuando un defensa coge una pelota por el bateada antes de que toque el suelo, Cuando el defensa toca la primera base con la pelota en su poder antes de que el bateador llegue a pisarla, Cuando un defensa toca al bateador con la pelota antes de que llegue a base, Cuando viola alguna regla y el arbitro decide eliminarlo.</a></li>
	
	&nbsp <a><li> El bateador avanza a la base cuando tiene 4 bolas </a></li>

	&nbsp <a><li> El corredor es eliminado: cuando un defensa lo toca con la pelota estando fuera de la base, Cuando el defensa con la pelota en su poder toca primero la base a la cual se dirige el corredor o Decisión arbitral debido a una falta.  </a></li>
	
	&nbsp <a><li> Si un corredor se encuentra en una base que necesita un compañero suyo en avance, está obligado a correr </a> </li>
	
	&nbsp <a><li> Si el corredor es obligado se lo puede eliminar de 2 maneras: tocándolo con la pelota cuando no esta en base o pisando la base a la cual pretende llegar si se tiene la posesión de la pelota. </a> </li>

	&nbsp <a><li> Cuando se batea una pelota y los defensas la capturan sin que toque el suelo, el corredor toma la decision de correr o no correr. </a> </li>
	