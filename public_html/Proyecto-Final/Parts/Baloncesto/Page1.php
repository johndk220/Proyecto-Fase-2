	&nbsp <a><li>A las cinco faltas personales, el jugador sancionado deberá ser sentado.</a></li>

	&nbsp <a><li>A las dos faltas antideportivas o a las dos faltas tecnicas, el jugador es expulsado del juego.</a></li>

	&nbsp <a><li>Las canastas tiradas detras de la línea de 3 valen tres puntos.</a></li>

