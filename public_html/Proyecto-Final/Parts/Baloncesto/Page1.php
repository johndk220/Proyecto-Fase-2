	&nbsp <a><li>Si un jugador está tirando, el jugador en defensa no puede tocar el aro.</a></li>

	&nbsp <a><li> Hay 24 segundos para tirar a canasta.</a></li>
	
	&nbsp <a><li> Tienes 8 segundos de tiempo para pasar la mitad de la cancha. </a></li>

	&nbsp <a><li>El tiro libre anotado vale un punto.</a></li>
	
	&nbsp <a><li>Está permitido dar 2 pasos sin driblar. </a></li>
	
	&nbsp <a><li>Los jugadores deben llevar la camiseta por dentro del pantalón. </a></li>
	
